in1 = int(input("Enter First Number "))
op= input("Enter Your Operator")
in2= int(input("Enter Your Sec Number"))

if op == "+":
    print(in1 + in2)

elif op == "-":
    print(in1 + in2)
elif op == "*":
    print(in1*in2)
elif op == "/":
    print(in1/in2)
elif op == "%":
    print(in1%in2)
else :
    print("UnAppropriate Operator")
